def read_file(file_name):
    """
    ( ͡° ͜ʖ ͡°)
    """
    file_path = os.path.join(os.getcwd(), file_name)
    file = open(file_path,"r")
    file = file.read().splitlines()
    return file

def names_to_dict(names):
    """
    ( ͡° ͜ʖ ͡°)
    """
    return_dict = dict()
    for char in range(65,91): #ASCII codes for capital letters
        for name in names:
            if char == ord(name[0].upper()):
                if chr(char) in return_dict.keys():
                    return_dict[chr(char)].append(name)
                else:
                    return_dict[chr(char)] = list()
                    return_dict[chr(char)].append(name)
    return return_dict

def frequency_counter(classroom):
    """
    ( ͡° ͜ʖ ͡°)
    """
    frequency_dict = dict()
    right = len(classroom)-1
    left = 0
    while left <= right:
        if classroom[left][0] == classroom[right][0]:
            if not(classroom[left][0] in frequency_dict.keys()):
                frequency_dict[classroom[left][0]] = 0
            frequency_dict[classroom[left][0]] = len(classroom[left:right])+1
            left = right + 1
            right = len(classroom) - 1
        else:
            for i in range(right,left-1,-1):
                if classroom[right][0] != classroom[i][0]:
                    right = i
                    break
    return frequency_dict


